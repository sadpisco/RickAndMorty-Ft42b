const server = require('./server');
const { conn } = require('./database');
const PORT = 3015;

conn.sync({force: false}).then(()=> {
    server.listen(PORT, () => {
        console.log(`Server listening on port ${PORT}.`)
    });
}).catch( error => console.log(error));

